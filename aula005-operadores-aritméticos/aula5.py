"""
+, -, *, /, //, **, %, ()
"""

# Inteiros e floats
print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração +', 10 - 5)
print('Divisão /', 10 / 2)

# Strings
print('Multiplicação /', 10 * '2')
print('Adição /', '0' + '2')
print('Luiz' + ' ' + 'Otávio e ele tem ' + str(32) + ' anos')

# Divisão inteira
print(10.55 / 3.5)
print(10.55 // 3.5)
print(10.55 / 3)
print(10.55 // 3)
print(10 / 3)
print(10 // 3)

# Exponenciação
print(2 ** 10)

# Resto da divisão
print(10 % 3)

# Precedência e parênteses
print(5 + 2 * 10)
print((5 + 2) * 10)
